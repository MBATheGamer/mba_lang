package lexer_test

import (
	"testing"

	"github.com/MBATheGamer/mba_lang/lexer"
	"github.com/MBATheGamer/mba_lang/token"
)

type TestToken struct {
	expectedType    token.TokenType
	expectedLiteral string
}

var inputs = map[string]string{
	"first-test": `=+(){},;`,
	"second-test": `
let five = 5;
let ten = 10;
let add = fn(x, y) {
	x + y;
};

let result = fn add(five, ten);
	`,
	"third-test": `
let five = 5;
let ten = 10;
let add = fn(x, y) {
	x + y;
};

let result = fn add(five, ten);
!-/*5;
5 < 10 > 5;
	`,
	"forth-test": `
let five = 5;
let ten = 10;
let add = fn(x, y) {
	x + y;
};

let result = fn add(five, ten);
!-/*5;
5 < 10 > 5;

if (5 < 10) {
	return true;
} else {
	return false;
}
`,
}

func testInput(input string, tests []TestToken, t *testing.T) {
	var l = lexer.New(input)

	for i, tt := range tests {
		var tok = l.NextToken()

		if tok.Type != tt.expectedType {
			t.Fatalf("tests[%d] - tokentype wrong. expected=%q, got=%q",
				i, tt.expectedType, tok.Type,
			)
		}

		if tok.Literal != tt.expectedLiteral {
			t.Fatalf("tests[%d] - literal wrong. expected=%q, got=%q",
				i, tt.expectedLiteral, tok.Literal,
			)
		}
	}
}

func TestFirstNextToken(t *testing.T) {
	var tests = []TestToken{
		{token.ASSIGN, "="},
		{token.PLUS, "+"},
		{token.LPAREN, "("},
		{token.RPAREN, ")"},
		{token.LBRACE, "{"},
		{token.RBRACE, "}"},
		{token.COMMA, ","},
		{token.SEMICOLON, ";"},
		{token.EOF, ""},
	}

	testInput(inputs["first-test"], tests, t)
}

